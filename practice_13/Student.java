package practice_13;

public class Student {
    int id;
    int age;
    String name;

    // getter and setter for id
    public int getId()
    {
        return id;
    }
    public void setId(int id)
    {
        this.id = id;
    }


    //getter and setter for age
    public void setAge(int age)
    {
        this.age = age;
    }
    public int getAge()
    {
        return age;
    }


    //getter and setter for name
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }

}
